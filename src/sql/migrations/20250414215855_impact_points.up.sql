    'WORKSUBMIT',
    'DONATION',
    'VOLUNTEER',
    'OTHER'
);

CREATE TABLE impact_points (
    id uuid DEFAULT uuid_generate_v4() NOT NULL,
    total_points int DEFAULT 0 NOT NULL,
    user_id uuid NOT NULL,
    social_cause social_causes_type NOT NULL,
    social_cause_category sdg_type NOT NULL,
    type impact_points_type NOT NULL,
    access_id uuid,
    meta jsonb,
    created_at timestamp with time zone DEFAULT now() NOT NULL,
    FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    FOREIGN KEY (access_id) REFERENCES accesses(id) ON DELETE SET NULL
);